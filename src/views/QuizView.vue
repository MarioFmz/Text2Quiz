const route = useRoute()
const router = useRouter()
const { user } = useAuth()

const API_URL = import.meta.env.VITE_API_URL || 'http://localhost:3001'

const quizId = route.params.id as string

// OpenAI client
const openai = new OpenAI({
  apiKey: import.meta.env.VITE_OPENAI_API_KEY,
  dangerouslyAllowBrowser: true
})

const quiz = ref<any>(null)
const questions = ref<Question[]>([])
const currentQuestionIndex = ref(0)
const userAnswers = ref<Record<string, string>>({})
const showResults = ref(false)
const showSummary = ref(true) // Mostrar resumen al inicio
const loading = ref(true)
const submitting = ref(false)
const previousAttempts = ref<any[]>([])
const quizStartTime = ref<number>(0)
const quizTimeTaken = ref<number>(0)
const expandedAttemptId = ref<string | null>(null)
const attemptAnswers = ref<Record<string, any>>({})
const loadingAttempt = ref<string | null>(null)
const showStudyMaterial = ref(false)

// Audio playback state
const isPlayingAudio = ref(false)
const isPausedAudio = ref(false)
const isGeneratingPodcast = ref(false)
const audioElement = ref<HTMLAudioElement | null>(null)
const audioBlobUrl = ref<string | null>(null)

// Regeneration modal
const showRegenerateModal = ref(false)
const regenerating = ref(false)

// Global leaderboard
const globalLeaderboard = ref<any[]>([])
const loadingLeaderboard = ref(false)

// Edit visibility modal
const showEditModal = ref(false)
const editingVisibility = ref(false)
const selectedVisibility = ref<'private' | 'public' | 'unlisted'>('private')
const selectedCategory = ref('')
const quizTags = ref<string[]>([])
const quizSource = ref('')
const tagInput = ref('')
const categories = ref<QuizCategory[]>([])

// Delete confirmation
const showDeleteConfirm = ref(false)
const deleting = ref(false)

// Notification modal
const showNotification = ref(false)
const notificationType = ref<'success' | 'error' | 'warning' | 'info'>('info')
const notificationMessage = ref('')
const notificationTitle = ref('')

const currentQuestion = computed(() => questions.value[currentQuestionIndex.value])
const progress = computed(() => ((currentQuestionIndex.value + 1) / questions.value.length) * 100)
const isLastQuestion = computed(() => currentQuestionIndex.value === questions.value.length - 1)

// Helper function to show notifications
const showNotif = (type: 'success' | 'error' | 'warning' | 'info', message: string, title?: string) => {
  notificationType.value = type
  notificationMessage.value = message
  notificationTitle.value = title || ''
  showNotification.value = true
}

onMounted(async () => {
  try {
    const data = await quizzesService.getQuizWithQuestions(quizId)
    quiz.value = data
    questions.value = data.questions

    // Cargar intentos previos si el usuario está autenticado
    if (user.value) {
      previousAttempts.value = await quizzesService.getQuizAttempts(user.value.id, quizId)
    }

    // Cargar categorías para el selector
    categories.value = await getCategories()

    // Cargar leaderboard global si el quiz tiene global_challenge_id
    if (quiz.value.global_challenge_id) {
      await loadGlobalLeaderboard()
    }
  } catch (error) {
    console.error('Error loading quiz:', error)
  } finally {
    loading.value = false
  }
})

const selectAnswer = (answer: string) => {
  if (!currentQuestion.value) return
  userAnswers.value[currentQuestion.value.id] = answer
}

const nextQuestion = () => {
  if (!isLastQuestion.value) {
    currentQuestionIndex.value++
  }
}

const previousQuestion = () => {
  if (currentQuestionIndex.value > 0) {
    currentQuestionIndex.value--
  }
}

const submitQuiz = async () => {
  if (!user.value) return

  submitting.value = true
  try {
    // Calcular tiempo tomado
    if (quizStartTime.value > 0) {
      quizTimeTaken.value = Math.floor((Date.now() - quizStartTime.value) / 1000)
    }

    // Guardar respuestas
    for (const question of questions.value) {
      const selectedAnswer = userAnswers.value[question.id]
      if (selectedAnswer) {
        await quizzesService.saveUserAnswer(
          user.value.id,
          quizId,
          question.id,
          selectedAnswer,
          question.correct_answer
        )
      }
    }

    // Calcular puntuación
    const correctCount = questions.value.filter(
      q => userAnswers.value[q.id] === q.correct_answer
    ).length

    // Crear snapshot del intento con preguntas y respuestas
    const attemptSnapshot = {
      questions: questions.value.map(q => ({
        id: q.id,
        question_text: q.question_text,
        question_type: q.question_type,
        options: q.options,
        correct_answer: q.correct_answer,
        explanation: q.explanation,
        user_answer: userAnswers.value[q.id] || null,
        is_correct: userAnswers.value[q.id] === q.correct_answer
      })),
      completed_at: new Date().toISOString(),
      time_taken: quizTimeTaken.value
    }

    // Guardar progreso con snapshot
    await quizzesService.saveProgress(
      user.value.id,
      quiz.value.document_id,
      quizId,
      correctCount,
      questions.value.length,
      attemptSnapshot
    )

    // Recargar intentos previos para actualizar la lista
    if (user.value) {
      previousAttempts.value = await quizzesService.getQuizAttempts(user.value.id, quizId)
    }

    // Guardar automáticamente en el global challenge si existe
    if (quiz.value.global_challenge_id) {
      try {
        await axios.post(
          `${API_URL}/api/challenges/${quiz.value.global_challenge_id}/attempt`,
          {
            userId: user.value.id,
            username: user.value.email?.split('@')[0] || 'Usuario',
            score: correctCount,
            totalQuestions: questions.value.length,
            timeTaken: quizTimeTaken.value
          }
        )
        console.log('Score saved to global leaderboard')

        // Recargar leaderboard
        await loadGlobalLeaderboard()
      } catch (error) {
        console.error('Error saving to global challenge:', error)
        // No mostrar error al usuario - el quiz se completó exitosamente
      }
    }

    showResults.value = true

    // Disparar confetti si es 100% de aciertos
    const percentage = Math.round((correctCount / questions.value.length) * 100)
    if (percentage === 100) {
      setTimeout(() => {
        triggerConfetti()
      }, 300)
    }
  } catch (error) {
    console.error('Error submitting quiz:', error)
  } finally {
    submitting.value = false
  }
}

const calculateResults = () => {
  const correctCount = questions.value.filter(
    q => userAnswers.value[q.id] === q.correct_answer
  ).length

  return {
    correct: correctCount,
    incorrect: questions.value.length - correctCount,
    percentage: Math.round((correctCount / questions.value.length) * 100)
  }
}

const restartQuiz = async () => {
  userAnswers.value = {}
  currentQuestionIndex.value = 0
  showResults.value = false
  showSummary.value = true // Volver a mostrar el resumen

  // Recargar intentos previos y leaderboard
  if (user.value) {
    previousAttempts.value = await quizzesService.getQuizAttempts(user.value.id, quizId)
  }
  if (quiz.value?.global_challenge_id) {
    await loadGlobalLeaderboard()
  }
}

const goToDocuments = () => {
  router.push('/documents')
}

const startQuiz = () => {
  // Si el usuario ya completó el quiz antes, preguntar si quiere nuevas preguntas
  if (previousAttempts.value.length > 0) {
    showRegenerateModal.value = true
  } else {
    // Primera vez, comenzar directamente
    beginQuiz()
  }
}

const beginQuiz = () => {
  showSummary.value = false
  showRegenerateModal.value = false
  quizStartTime.value = Date.now()
}

const regenerateAndStart = async () => {
  if (!user.value) return

  regenerating.value = true
  try {
    const regeneratedData = await quizzesService.regenerateQuestions(quizId, user.value.id)
    questions.value = regeneratedData.questions
    console.log('Preguntas regeneradas exitosamente')
    beginQuiz()
  } catch (error: any) {
    console.error('Error regenerando preguntas:', error)
    // Mostrar error amigable y continuar con preguntas existentes
    const errorMessage = error?.response?.data?.error || error.message
    if (errorMessage?.includes('No content available')) {
      console.warn('Este quiz no tiene el contenido guardado para regenerar preguntas. Usando las preguntas existentes.')
    }
    // Si falla la regeneración, usar las preguntas existentes
    beginQuiz()
  } finally {
    regenerating.value = false
  }
}

const formatDate = (dateString: string) => {
  return new Date(dateString).toLocaleDateString('es-ES', {
    year: 'numeric',
    month: 'short',
    day: 'numeric',
    hour: '2-digit',
    minute: '2-digit'
  })
